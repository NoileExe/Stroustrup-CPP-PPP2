vector<int> game_ai(int elem_count)
/*–ó–∞–ø–æ–ª–Ω–µ–Ω–∏–µ –≤–µ–∫—Ç–æ—Ä–∞ —Å–ª—É—á–∞–π–Ω—ã–º–∏ –Ω–µ–ø–æ–≤—Ç–æ—Ä—è—é—â–∏–º–∏—Å—è —á–∏—Å–ª–∞–º–∏.
–í–æ–∑–≤—Ä–∞—â–∞–µ—Ç –≤–µ–∫—Ç–æ—Ä –¶–ï–õ–´–• –ü–û–õ–û–ñ–ò–¢–ï–õ–¨–ù–´–• –ù–ï–ü–û–í–¢–û–†–Ø–Æ–©–ò–•–°–Ø —á–∏—Å–µ–ª (–æ—Ç 0 –¥–æ 9)*/
{
	vector<int> res (elem_count);
	int buff = 0; //–ë—É—Ñ—Ñ–µ—Ä
	srand( time(NULL) );
	
	for (int i = 0; i < elem_count; ++i)
	{
		buff = rand() % 10;
				
		for (int j = 0; j <= i; ++j) //–ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞ –ø–æ–≤—Ç–æ—Ä—ã
			//–ï—Å–ª–∏ —á–∏—Å–ª–æ –µ—â—ë –Ω–µ –∑–∞–Ω–æ—Å–∏–ª–æ—Å—å –≤ –≤–µ–∫—Ç–æ—Ä –∏ –ø—Ä–æ–≤–µ—Ä–∫–∞ –≤—Å–µ—Ö 
			// —ç–ª–µ–º–µ–Ω—Ç–æ–≤ –≤–µ–∫—Ç–æ—Ä–∞ –Ω–∞ —ç—Ç–æ —É—Å–ª–æ–≤–∏–µ –ø—Ä–æ—à–ª–∞ —É—Å–ø–µ—à–Ω–æ
			if (buff != res[j] && j == i)
				res[i] = buff;
			//–ï—Å–ª–∏ —á–∏—Å–ª–æ —É–∂–µ –∏–º–µ–µ—Ç—Å—è –≤ –≤–µ–∫—Ç–æ—Ä–µ
			else if (buff == res[j])
			{
				--i;
				break; //–ü–æ–≤—Ç–æ—Ä—è–µ–º –ø–æ–ø—ã—Ç–∫—É –∑–∞–Ω–µ—Å—Ç–∏ —Å–ª—É—á–∞–π–Ω–æ–µ —á–∏—Å–ª–æ
			}
	}
	
	return res;
}
