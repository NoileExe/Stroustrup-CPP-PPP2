double common_check (const vector<double>& price, const vector<double>& count)
//–≤—ã—á–∏—Å–ª—è–µ—Ç —Å—É–º–º—É –ø—Ä–æ–∏–∑–≤–µ–¥–µ–Ω–∏–π –¥–≤—É—Ö –ø–∞—Ä–∞–ª–ª–µ–ª—å–Ω—ã—Ö –∑–Ω–∞—á–µ–Ω–∏–π –≤–µ–∫—Ç–æ—Ä–æ–≤ (—Ü–µ–Ω–∞*–∫–æ–ª-–≤–æ_—Ç–æ–≤–∞—Ä–∞)
//–ù–∞ –≤—Ö–æ–¥: –∫–æ–Ω—Å—Ç–∞–Ω—Ç–Ω—ã–µ —Å—Å—ã–ª–∫–∏ –Ω–∞ 2 –Ω–µ–ø—É—Å—Ç—ã—Ö –≤–µ–∫—Ç–æ—Ä–∞ —Ä–∞–≤–Ω—ã—Ö –ø–æ —Ä–∞–∑–º–µ—Ä–∞–º
//–í–æ–∑–≤—Ä–∞—Ç: —Å—É–º–º–∞ –ø—Ä–æ–∏–∑–≤–µ–¥–µ–Ω–∏–π –¥–≤—É—Ö –ø–∞—Ä–∞–ª–ª–µ–ª—å–Ω—ã—Ö –∑–Ω–∞—á–µ–Ω–∏–π –≤–µ–∫—Ç–æ—Ä–æ–≤ (price[i]*count[i])
{
	if (price.size()==0 || count.size()==0)
		my_error ("–ø—É—Å—Ç–æ–π –≤–µ–∫—Ç–æ—Ä!!!");
	else if (price.size() != count.size())
		my_error ("—Ä–∞–∑–º–µ—Ä—ã –≤–µ–∫—Ç–æ—Ä–æ–≤ '—Ü–µ–Ω–∞' –∏ '–∫–æ–ª-–≤–æ (–≤–µ—Å) —Ç–æ–≤–∞—Ä–∞' –Ω–µ —Ä–∞–≤–Ω—ã!!!");
		
	double summ = 0;
	for (int i = 0;  i < price.size(); ++i)
		summ += price[i]*count[i];
		
	if (isinf(summ))
		my_error ("–æ–±—â–∞—è —Å—É–º–º–∞ —á–µ–∫–∞ –≤—ã—à–ª–∞ –∑–∞ –ø—Ä–µ–¥–µ–ª—ã —Ç–∏–ø–∞ DOUBLE!!!");
	else return summ;
}

